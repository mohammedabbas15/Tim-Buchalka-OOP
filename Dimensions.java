public class Dimensions
{
    private int height;
    private int depth;
    private int width;

    public Dimensions(int height, int depth, int width) {
        this.height = height;
        this.depth = depth;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }

    public int getWidth() {
        return width;
    }
}
